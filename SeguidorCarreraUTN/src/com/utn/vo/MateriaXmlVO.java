package com.utn.vo;


public class MateriaXmlVO 
{
	private String id;
	private String nombre;
	private String porCursar;
	private String porAprobar;
	private String habilitaCursar;
	private String habilitaAprobar;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getPorCursar() {
		return porCursar;
	}
	public void setPorCursar(String porCursar) {
		this.porCursar = porCursar;
	}
	public String getPorAprobar() {
		return porAprobar;
	}
	public void setPorAprobar(String porAprobar) {
		this.porAprobar = porAprobar;
	}
	public String getHabilitaCursar() {
		return habilitaCursar;
	}
	public void setHabilitaCursar(String habilitaCursar) {
		this.habilitaCursar = habilitaCursar;
	}
	public String getHabilitaAprobar() {
		return habilitaAprobar;
	}
	public void setHabilitaAprobar(String habilitaAprobar) {
		this.habilitaAprobar = habilitaAprobar;
	}
	
}
